package dal;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author SaiBack
 */
@Entity
@Table(name = "visited")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Visited.findAll", query = "SELECT v FROM Visited v")
    , @NamedQuery(name = "Visited.findById", query = "SELECT v FROM Visited v WHERE v.id = :id")
    , @NamedQuery(name = "Visited.findByLink", query = "SELECT v FROM Visited v WHERE v.link = :link")})
public class Visited implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "link")
    private String link;

    public Visited() {
    }

    public Visited(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Visited)) {
            return false;
        }
        Visited other = (Visited) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "dal.Visited[ id=" + id + " ]";
    }
    
}
